// Strings o Cadenas de Textos METODOS 

const tweet = "Aprendiendo a programar en JS en el curso de desarrollo web completo";

console.log(tweet.length)

const producto2 = "Monitor HD";

console.log(producto2)


//IndexOf (retorna posicion)

console.log(tweet.indexOf('JS'));
console.log(producto2.indexOf('tablet'));


console.log(tweet.includes('JS'));
console.log(producto2.includes('tablet'));



/*
Metodos: 
length indica cuantos caracteres se tienen  ejemplo console.log(producto.length)
indexOF comprobar si una palabra se encuentra en el string  ejemplo  console.log(tweet.indexOf('JS'));
includes hace lo mismo que indexOf pero con la diferencia que te indica con true o false 
*/